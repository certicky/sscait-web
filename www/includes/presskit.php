<p>
 This being a strictly AI vs AI tournament, humans will only be coding and handling the servers. 
 In 2015, Russian player Djem5 played a couple of demonstration matches, in which he summarily beat 
 all AI opposition without breaking a sweat. 
 In the current state of Broodwar AI, bots tend to lose because they're not as adaptive as humans yet, so they can be 
 tricked by abusing situations they are not yet able to handle.
</p>
<h4>What this year's tournament is all about</h4>
<p>
Every year we see an increase in the amount of actual AI involved in the matches. 
While most of the older bots have hard-coded plans of what to build when, where to attack and 
how, newer bots apply a mixture of those methods and adaptive programming. They are able to respond to 
the adversary's air-units by building anti-air weapons or to delay the production of 
combat units in favour of economic expansion, based on scouting information. Thus, they will have a 
stronger economy, and a better chance of victory in the long run. A handful of bots actively 
engage in learning strategies, like Martin Rooijackers' LetaBot, Dave Churchill's UAlbertaBot, 
tscmoo and others who employ the machine learning algorithms that allow the AI to learn from watching 
replays of earlier played games, or simply by playing the game a lot. 
The most successful AI is usually the one with the optimal balance between a good plan, 
clever heuristics and fastest learning capabilities.</p>
