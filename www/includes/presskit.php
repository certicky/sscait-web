<h4><a name="whatis">What is SSCAIT?</a></h4>
<p>The Student StarCraft AI Tournament is an educational event, first held in 2011. It serves as a challenging competitive environment for students of Artificial Intelligence and Computer Science, as well as other AI and Broodwar enthousiasts. They are submitting the bots, which are programmed in C++ or Java using the custom API BWAPI to play 1v1 StarCraft matches against one another.</p>
<p>Since the tournament already has quite a history, we usually invite back a number of participants that have been in the scene from the very start, and of course numerous newly developed entrants hope to achieve big things, or add a little glamour to their BA/MA theses or CVs.</p>
