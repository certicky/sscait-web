<h2>SSCAIT 2018/19</h2>
<div>
   There were <b>72 bots</b> in the 2018/19 installment of SSCAIT. The tournament was divided into two phases:
   <ol>
      <li><b>Round Robin tournament</b> of 5112 games, where everybody played two games against everybody else. 
         After this, 16 best bots proceeded into next phase and we announced the best student-created bot (the one with the most points among student-created submissions).
      <li><b>Double elimination bracket</b> of 16 best bots.
   </ol>
</div>
