<h2>SSCAIT 2014/15</h2>
<div>
The 2014/15 installment of SSCAIT sported 42 participants. The tournament was divided into two divisions:
<ul>
	<li><b>Student division</b>: Round Robin tournament of 861 games, where everybody played two matches against everybody else. 
	Only the students were eligible for prizes.
	<li><b>Mixed division</b>: Elimination bracket of 8 best bots (including non-students and teams).
</ul>
