<h2>SSCAIT 2020/21</h2>
<div>
   There were <b>56 bots</b> in the 2020/21 installment of SSCAIT. The tournament was divided into two phases:
   <ol>
      <li><b>Round Robin tournament</b> of 3080 games, where everybody played two games against everybody else. After this, 16 bots with the best score proceeded into next phase.
      <li><b>Double elimination bracket</b> of the 16 best bots.
   </ol>
</div>
