2012/13 was the second installment of SSCAI tournament with 52 participants. The tournament was divided into two divisions:
<ul>
	<li><b>Student division</b>: Round Robin tournament, where everybody played one match against everobody. However, only individual students could score points and win prizes.
	<li><b>Mixed division</b>: Elimination bracket of 8 best bots (including non-students and teams).
</ul>
