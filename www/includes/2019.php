<h2>SSCAIT 2019/20</h2>
<div>
   There were <b>45 bots</b> in the 2019/20 installment of SSCAIT. The tournament was divided into two phases:
   <ol>
      <li><b>Round Robin tournament</b> of 1980 games, where everybody played two games against everybody else. After this, 16 bots with the best score proceeded into next phase.
      <li><b>Double elimination bracket</b> of the 16 best bots.
   </ol>
